sap.ui.define([
    "sap/ui/core/mvc/Controller",
    "sap/ui/model/json/JSONModel",
    "sap/ui/model/Filter",
    "sap/ui/model/FilterOperator",
    "sap/m/MessageToast",
    "sap/m/MessageBox"
], function (Controller, JSONModel, Filter, FilterOperator, MessageToast, MessageBox) {
    "use strict";

    return Controller.extend("lojacap.controller.payment-page", {

        onInit: function () {
            console.log("游눯 Controller payment-page.js: onInit - Iniciando.");
            // 1. Modelo para os dados da view de pagamento
            const oPaymentViewModel = new JSONModel({
                itens: [],
                valorTotal: "0.00",
                clienteID: null,        // Ser치 buscado
                itensNoCarrinho: false, // Para controlar se h치 itens
                isBuyNowFlow: false    // Flag para diferenciar os fluxos
            });
            this.getView().setModel(oPaymentViewModel, "paymentView");

            // 2. Modelo para os tipos de pagamento
            const oEnumPagamento = new JSONModel([
                { key: "PIX", text: "PIX" },
                { key: "CARTAO", text: "Cart칚o" },
                { key: "BOLETO", text: "Boleto" }
            ]);
            this.getView().setModel(oEnumPagamento, "enum");

            // 3. Anexa uma fun칞칚o para ser chamada quando a rota "RoutePayment" for acessada
            const oRouter = this.getOwnerComponent().getRouter();
            oRouter.getRoute("RoutePayment").attachPatternMatched(this._onRouteMatched, this);
            console.log("游눯 Controller payment-page.js: onInit - Rota 'RoutePayment' monitorada.");
        },

        _onRouteMatched: function (oEvent) {
            console.log("游눯 Controller payment-page.js: _onRouteMatched - Rota acessada.");
            const oPaymentViewModel = this.getView().getModel("paymentView");
            const oMainModel = this.getOwnerComponent().getModel(); // Modelo OData principal

            // Resetar a view model para evitar dados de navega칞칫es anteriores no mesmo view instance
            oPaymentViewModel.setProperty("/itens", []);
            oPaymentViewModel.setProperty("/valorTotal", "0.00");
            oPaymentViewModel.setProperty("/clienteID", null);
            oPaymentViewModel.setProperty("/itensNoCarrinho", false);
            oPaymentViewModel.setProperty("/isBuyNowFlow", false); // Reset da flag
            console.log("游눯 Controller payment-page.js: paymentViewModel resetado para nova entrada na rota.");


            const oNavArgsModel = this.getOwnerComponent().getModel("navArgs");
            const oNavData = oNavArgsModel.getData();
            console.log("游눯 Controller payment-page.js: Dados recebidos via modelo 'navArgs':", JSON.stringify(oNavData));

            const sBuyNowProductId = oNavData.buyNowProductId;

            if (sBuyNowProductId) { // FLUXO "COMPRAR AGORA"
                console.log("游눯 Controller payment-page.js: Fluxo 'Comprar Agora' DETECTADO. Produto ID:", sBuyNowProductId);
                oPaymentViewModel.setProperty("/isBuyNowFlow", true);

                const fBuyNowProductPreco = parseFloat(oNavData.buyNowProductPreco);
                const iBuyNowProductQty = parseInt(oNavData.buyNowProductQty, 10);
                const sBuyNowProdutoNome = oNavData.buyNowProdutoNome;

                // Limpa o navArgs AP칍S ler os dados
                oNavArgsModel.setData({});
                console.log("游눯 Controller payment-page.js: Modelo 'navArgs' limpo.");

                const oItemUnico = {
                    produto_ID: sBuyNowProductId,
                    quantidade: iBuyNowProductQty,
                    precoUnitario: fBuyNowProductPreco,
                    produto: {
                        ID: sBuyNowProductId,
                        nome: sBuyNowProdutoNome,
                        preco: fBuyNowProductPreco
                    }
                };

                oPaymentViewModel.setProperty("/itens", [oItemUnico]);
                oPaymentViewModel.setProperty("/valorTotal", (fBuyNowProductPreco * iBuyNowProductQty).toFixed(2));
                oPaymentViewModel.setProperty("/itensNoCarrinho", true); // H치 um item para pagar

                // ### IN칈CIO DA MUDAN칂A CR칈TICA PARA "COMPRAR AGORA" ###
                // No fluxo "Comprar Agora", o clienteID DEVE vir diretamente dos dados do usu치rio logado (localStorage).
                const sClienteIDLogado = localStorage.getItem("clienteID");
                console.log("游눯 Controller payment-page.js (Comprar Agora): Tentando obter clienteID do localStorage. Valor:", sClienteIDLogado);

                if (sClienteIDLogado) {
                    oPaymentViewModel.setProperty("/clienteID", sClienteIDLogado);
                    console.log("游눯 Controller payment-page.js (Comprar Agora): Cliente ID (localStorage) definido para pagamento:", sClienteIDLogado);
                } else {
                    // Se n칚o h치 clienteID no localStorage, o usu치rio n칚o est치 logado ou houve um problema no login.
                    console.error("游눯 Controller payment-page.js (Comprar Agora): CR칈TICO - clienteID n칚o encontrado no localStorage. O usu치rio n칚o est치 devidamente logado.");
                    MessageBox.error("Seus dados de login n칚o foram encontrados. Por favor, fa칞a login novamente para continuar.", {
                        onClose: () => this.getOwnerComponent().getRouter().navTo("RouteLogin") // Redireciona para o login
                    });
                    // Interrompe o fluxo aqui, pois sem clienteID n칚o podemos prosseguir
                    return;
                }
                // ### FIM DA MUDAN칂A CR칈TICA PARA "COMPRAR AGORA" ###

            } else { // FLUXO CARRINHO COMPLETO
                console.log("游눯 Controller payment-page.js: Fluxo 'Carrinho Completo' detectado.");
                oPaymentViewModel.setProperty("/isBuyNowFlow", false);

                this.carrinhoID = localStorage.getItem("carrinhoID"); // Usa o carrinhoID da sess칚o
                if (!this.carrinhoID) {
                    console.warn("游눯 Controller payment-page.js (Carrinho Completo): Nenhum carrinhoID no localStorage.");
                    MessageBox.warning("Seu carrinho para pagamento est치 vazio ou n칚o foi encontrado. Adicione produtos ao carrinho.", {
                        onClose: () => this.getOwnerComponent().getRouter().navTo("RouteProdutos")
                    });
                    // Garante que a view reflita o estado de carrinho vazio
                    oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                    oPaymentViewModel.setProperty("/valorTotal", "0.00");
                    oPaymentViewModel.setProperty("/itens", []);
                    return; // Interrompe se n칚o h치 carrinho para carregar
                }

                const sCarrinhoPath = "/Carrinhos(ID=" + this.carrinhoID + ")";
                console.log("游눯 Controller payment-page.js (Carrinho Completo): Buscando Carrinho para obter cliente_ID e itens. Path:", sCarrinhoPath);
                const oCarrinhoContextBinding = oMainModel.bindContext(sCarrinhoPath);

                oCarrinhoContextBinding.requestObject().then(oCarrinhoData => {
                    console.log("游눯 Controller payment-page.js (Carrinho Completo): Dados do carrinho recebidos:", JSON.stringify(oCarrinhoData));
                    if (oCarrinhoData && oCarrinhoData.cliente_ID) {
                        oPaymentViewModel.setProperty("/clienteID", oCarrinhoData.cliente_ID);
                        console.log("游눯 Controller payment-page.js (Carrinho Completo): Cliente ID (via carrinho) definido:", oCarrinhoData.cliente_ID);
                        this._carregarItensEFinalizarCalculo(this.carrinhoID); // Carrega os itens do carrinho
                    } else {
                        // Este 칠 o cen치rio que causa o erro se o carrinhoID for de um carrinho an칪nimo sem cliente
                        console.warn("游눯 Controller payment-page.js (Carrinho Completo): N칚o foi poss칤vel obter cliente_ID do carrinho. Dados:", JSON.stringify(oCarrinhoData));
                        MessageBox.error("N칚o foi poss칤vel obter os dados do cliente para este carrinho. Verifique se o carrinho est치 correto ou fa칞a login.", {
                            onClose: () => this.getOwnerComponent().getRouter().navTo("RouteProdutos") // Ou RouteCarrinho
                        });
                        oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                    }
                }).catch(oError => {
                    console.error("游눯 Controller payment-page.js (Carrinho Completo): Erro ao buscar dados do Carrinho:", oError);
                    MessageBox.error("Erro ao carregar dados do carrinho. Tente novamente.");
                    oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                    oPaymentViewModel.setProperty("/valorTotal", "0.00");
                    oPaymentViewModel.setProperty("/itens", []);
                });
            }
        },

        _carregarItensEFinalizarCalculo: function (sCarrinhoID) {
            console.log("游눯 Controller payment-page.js: _carregarItensEFinalizarCalculo - Iniciando para carrinhoID:", sCarrinhoID);
            const oMainModel = this.getOwnerComponent().getModel();
            const oPaymentViewModel = this.getView().getModel("paymentView");

            // Busca os itens do carrinho especificado, expandindo os dados do produto
            const oListBinding = oMainModel.bindList("/ItemCarrinho", undefined,
                undefined,
                [new Filter("carrinho_ID", FilterOperator.EQ, sCarrinhoID)],
                { $expand: "produto" }
            );

            oListBinding.requestContexts().then(aContexts => {
                const aItens = aContexts.map(oContext => oContext.getObject());
                console.log("游눯 Controller payment-page.js: _carregarItensEFinalizarCalculo - Itens do carrinho carregados:", JSON.stringify(aItens));
                oPaymentViewModel.setProperty("/itens", aItens);

                let totalCalculado = 0;
                if (aItens.length > 0) {
                    oPaymentViewModel.setProperty("/itensNoCarrinho", true);
                    aItens.forEach(item => {
                        // Garante que precoUnitario e quantidade s칚o v치lidos para o c치lculo
                        const preco = parseFloat(item.produto?.preco || item.precoUnitario || 0); // Prioriza o pre칞o do produto expandido
                        const qtd = parseInt(item.quantidade, 10) || 0;
                        totalCalculado += preco * qtd;
                    });
                    console.log("游눯 Controller payment-page.js: _carregarItensEFinalizarCalculo - Total calculado:", totalCalculado);
                } else {
                    console.log("游눯 Controller payment-page.js: _carregarItensEFinalizarCalculo - Nenhum item encontrado para o carrinhoID:", sCarrinhoID);
                    oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                }
                oPaymentViewModel.setProperty("/valorTotal", totalCalculado.toFixed(2));

                if (!oPaymentViewModel.getProperty("/itensNoCarrinho")) {
                    MessageBox.warning("Seu carrinho para pagamento est치 vazio. Adicione produtos.", {
                        onClose: () => {
                            this.getOwnerComponent().getRouter().navTo("RouteProdutos");
                        }
                    });
                }
            }).catch(oError => {
                console.error("游눯 Controller payment-page.js: _carregarItensEFinalizarCalculo - Erro ao carregar itens:", oError);
                MessageBox.error("N칚o foi poss칤vel carregar os itens do seu carrinho para pagamento.");
                oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                oPaymentViewModel.setProperty("/valorTotal", "0.00");
                oPaymentViewModel.setProperty("/itens", []);
            });
        },

        onFinalizarCompra: async function () {
            console.log("游눯 Controller payment-page.js: onFinalizarCompra - Iniciando.");
            const oPaymentViewModel = this.getView().getModel("paymentView");

            const sTipoPagamento = this.byId("selectPayment").getSelectedKey(); // Supondo que o ID do seu Select 칠 "selectPayment"
            const sClienteID = oPaymentViewModel.getProperty("/clienteID");
            const oMainODataModel = this.getView().getModel();

            if (!sTipoPagamento) { MessageBox.warning("Escolha uma forma de pagamento."); return; }
            if (!sClienteID) {
                MessageBox.error("ID do cliente n칚o est치 dispon칤vel. N칚o 칠 poss칤vel finalizar o pedido.");
                console.error("游눯 Controller payment-page.js: ClienteID faltando para finalizar compra.");
                return;
            }
            if (!oPaymentViewModel.getProperty("/itensNoCarrinho") || !oPaymentViewModel.getProperty("/itens") || oPaymentViewModel.getProperty("/itens").length === 0) {
                MessageBox.error("N칚o h치 itens para finalizar o pedido.");
                console.error("游눯 Controller payment-page.js: Nenhum item na view de pagamento para finalizar.");
                return;
            }

            const bIsBuyNowFlow = oPaymentViewModel.getProperty("/isBuyNowFlow");
            let oAction;
            let sActionPath; // Para log

            if (bIsBuyNowFlow) {
                sActionPath = "/realizarPagamentoItemUnico(...)";
                console.log("游눯 Controller payment-page.js (Finalizar Compra - Comprar Agora): Chamando a칞칚o 'realizarPagamentoItemUnico'.");
                const oItemUnico = oPaymentViewModel.getProperty("/itens")[0];

                oAction = oMainODataModel.bindContext(sActionPath);
                oAction.setParameter("clienteID", sClienteID);
                oAction.setParameter("tipoPagamento", sTipoPagamento);
                oAction.setParameter("produtoID", oItemUnico.produto.ID); // ID do produto
                oAction.setParameter("quantidade", oItemUnico.quantidade); // Quantidade
                // Sua a칞칚o 'realizarPagamentoItemUnico' parece esperar 'precoUnitario' tamb칠m.
                // Se o pre칞o j치 est치 no produto, voc칡 pode busc치-lo ou passar como est치.
                // Se sua action recalcula o pre칞o no backend baseado no produtoID, talvez n칚o precise enviar.
                // Vou manter como no seu c칩digo original.
                oAction.setParameter("precoUnitario", oItemUnico.precoUnitario);

                console.log("游눯 Controller payment-page.js: Par칙metros para realizarPagamentoItemUnico:", {
                    clienteID: sClienteID, tipoPagamento: sTipoPagamento, produtoID: oItemUnico.produto.ID,
                    quantidade: oItemUnico.quantidade, precoUnitario: oItemUnico.precoUnitario
                });

            } else { // Fluxo Carrinho Completo
                sActionPath = "/realizarPagamento(...)";
                console.log("游눯 Controller payment-page.js (Finalizar Compra - Carrinho): Chamando a칞칚o 'realizarPagamento'.");
                oAction = oMainODataModel.bindContext(sActionPath);
                // A a칞칚o 'realizarPagamento' no seu c칩digo original espera 'clienteID'.
                // Se o carrinhoID tamb칠m for necess치rio no backend para identificar os itens,
                // sua action precisa lidar com isso a partir do clienteID ou voc칡 precisaria enviar carrinhoID tamb칠m.
                // Mantendo como no seu c칩digo:
                oAction.setParameter("clienteID", sClienteID);
                oAction.setParameter("tipoPagamento", sTipoPagamento);
                // Se a action precisar do carrinhoID para identificar os itens do carrinho espec칤fico daquele cliente:
                // oAction.setParameter("carrinhoID", this.carrinhoID); // this.carrinhoID foi definido no _onRouteMatched para este fluxo

                console.log("游눯 Controller payment-page.js: Par칙metros para realizarPagamento:", {
                    clienteID: sClienteID, tipoPagamento: sTipoPagamento
                    // carrinhoID: this.carrinhoID // Adicione se necess치rio
                });
            }

            MessageToast.show("Processando seu pedido...");

            try {
                console.log("游눯 Controller payment-page.js: Executando a칞칚o OData:", sActionPath);
                // O resultado da action (sPedidoID) depende de como sua action OData V4 retorna o valor.
                // Se for um tipo complexo, 칠 oActionResult.getObject().<propriedade>.
                // Se for um tipo primitivo diretamente, pode ser oActionResult.getProperty("value") ou similar.
                // Seu c칩digo original esperava o ID diretamente.
                const oActionResult = await oAction.execute();
                let sPedidoID;

                // Tentativa de obter o PedidoID da resposta da Action
                if (oActionResult) {
                    if (typeof oActionResult.getObject === "function" && oActionResult.getObject() && oActionResult.getObject().pedidoID) {
                        sPedidoID = oActionResult.getObject().pedidoID;
                         console.log("游눯 Controller payment-page.js: Pedido ID obtido de getObject().pedidoID:", sPedidoID);
                    } else if (oActionResult.getProperty && typeof oActionResult.getProperty("value") === 'string') { // Se a action retorna uma string simples
                        sPedidoID = oActionResult.getProperty("value");
                         console.log("游눯 Controller payment-page.js: Pedido ID obtido de getProperty('value') (string):", sPedidoID);
                    } else if (oActionResult.getProperty && typeof oActionResult.getProperty("value") === 'object' && oActionResult.getProperty("value")?.pedidoID) { // Se value 칠 um objeto
                        sPedidoID = oActionResult.getProperty("value").pedidoID;
                        console.log("游눯 Controller payment-page.js: Pedido ID obtido de getProperty('value').pedidoID (objeto):", sPedidoID);
                    } else {
                        console.warn("游눯 Controller payment-page.js: Formato de resposta da action n칚o reconhecido para PedidoID. Resposta completa:", JSON.stringify(oActionResult.getObject ? oActionResult.getObject() : oActionResult));
                    }
                } else {
                     console.warn("游눯 Controller payment-page.js: A칞칚o OData n칚o retornou resultado (oActionResult 칠 nulo/undefined).");
                }


                if (sPedidoID) {
                    MessageToast.show("Pedido realizado com sucesso! ID: " + sPedidoID);
                } else {
                    MessageToast.show("Pedido processado! (ID do pedido n칚o recuperado da resposta).");
                    console.warn("游눯 Controller payment-page.js: Pedido processado, mas ID do pedido n칚o foi claramente extra칤do da resposta da action.");
                }


                if (!bIsBuyNowFlow) {
                    console.log("游눯 Controller payment-page.js: Finaliza칞칚o de Carrinho Completo: Removendo carrinhoID do localStorage.");
                    localStorage.removeItem("carrinhoID");
                    this.carrinhoID = null; // Limpa a propriedade do controller tamb칠m
                } else {
                    console.log("游눯 Controller payment-page.js: Finaliza칞칚o de 'Comprar Agora': carrinhoID do localStorage n칚o 칠 removido aqui.");
                }

                // Limpeza do ViewModel da P치gina de Pagamento ap칩s sucesso
                oPaymentViewModel.setProperty("/itensNoCarrinho", false);
                oPaymentViewModel.setProperty("/itens", []);
                oPaymentViewModel.setProperty("/valorTotal", "0.00");
                oPaymentViewModel.setProperty("/clienteID", null);
                oPaymentViewModel.setProperty("/isBuyNowFlow", false); // Reseta a flag

                this.getOwnerComponent().getRouter().navTo("Routehome-page"); // Navega para a home

            } catch (err) {
                const sErrorMessage = err.message || "Erro desconhecido ao processar o pedido.";
                MessageBox.error("Erro ao finalizar pedido: " + sErrorMessage);
                console.error("游눯 Controller payment-page.js: Erro na execu칞칚o da a칞칚o OData:", err, JSON.stringify(err));

                if (oAction && typeof oAction.resetChanges === 'function') {
                    oAction.resetChanges();
                }
            }
        },

        onPrintOrder: function () {
            console.log("游눯 Controller payment-page.js: onPrintOrder chamado.");
            var oPaymentViewModel = this.getView().getModel("paymentView");
            var oPaymentViewData = oPaymentViewModel.getData();

            if (!oPaymentViewData || !oPaymentViewData.itens || oPaymentViewData.itens.length === 0) {
                MessageToast.show("N칚o h치 itens no pedido para imprimir.");
                return;
            }

            var sPrintContent = "<h1>Detalhes do Pedido</h1>";
            sPrintContent += "<p><strong>Data:</strong> " + new Date().toLocaleDateString() + "</p>";
            sPrintContent += "<p><strong>Hora:</strong> " + new Date().toLocaleTimeString() + "</p>";
            sPrintContent += "<h2>Itens:</h2>";
            sPrintContent += "<table border='1' style='width:100%; border-collapse: collapse;'>";
            sPrintContent += "<thead><tr><th>Produto</th><th>Pre칞o Unit치rio</th><th>Quantidade</th><th>Total</th></tr></thead>";
            sPrintContent += "<tbody>";

            oPaymentViewData.itens.forEach(function (oItem) {
                const precoUnit = parseFloat(oItem.produto?.preco || oItem.precoUnitario || 0);
                const qtd = parseInt(oItem.quantidade, 10) || 0;
                var fItemTotal = precoUnit * qtd;
                sPrintContent += "<tr>";
                sPrintContent += "<td>" + (oItem.produto ? oItem.produto.nome : "N/A") + "</td>";
                sPrintContent += "<td>" + precoUnit.toFixed(2) + "</td>";
                sPrintContent += "<td>" + qtd + "</td>";
                sPrintContent += "<td>" + fItemTotal.toFixed(2) + "</td>";
                sPrintContent += "</tr>";
            });

            sPrintContent += "</tbody></table>";
            sPrintContent += "<h3>Valor Total do Pedido: " + (parseFloat(oPaymentViewData.valorTotal) || 0).toFixed(2) + "</h3>";

            var printWindow = window.open('', '_blank');
            printWindow.document.write('<html><head><title>Imprimir Pedido</title>');
            printWindow.document.write('<style>');
            printWindow.document.write('body { font-family: Arial, sans-serif; margin: 20px; }');
            printWindow.document.write('h1, h2, h3 { color: #333; }');
            printWindow.document.write('table { width: 100%; border-collapse: collapse; margin-top: 20px; }');
            printWindow.document.write('th, td { border: 1px solid #ddd; padding: 8px; text-align: left; }');
            printWindow.document.write('th { background-color: #f2f2f2; }');
            printWindow.document.write('</style>');
            printWindow.document.write('</head><body>');
            printWindow.document.write(sPrintContent);
            printWindow.document.write('</body></html>');
            printWindow.document.close();
            printWindow.print();
        },

        onNavToCart: function () {
            console.log("游눯 Controller payment-page.js: Navegando para RouteCarrinho.");
            this.getOwnerComponent().getRouter().navTo("RouteCarrinho");
        }
    });
});